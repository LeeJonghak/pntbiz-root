package core.wms.contents.domain;

public class ContentsTypeComponent {
	
	private Integer typeNum;
	private Integer compNum;
	private String compType;
	private String compField;
	private String compName;
	private String langCode;
	private Integer orderNum;
	
	public Integer getTypeNum() {
		return typeNum;
	}
	public void setTypeNum(Integer typeNum) {
		this.typeNum = typeNum;
	}
	public Integer getCompNum() {
		return compNum;
	}
	public void setCompNum(Integer compNum) {
		this.compNum = compNum;
	}
	public String getCompType() {
		return compType;
	}
	public void setCompType(String compType) {
		this.compType = compType;
	}
	public String getCompField() {
		return compField;
	}
	public void setCompField(String compField) {
		this.compField = compField;
	}
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
	public String getLangCode() {
		return langCode;
	}
	public void setLangCode(String langCode) {
		this.langCode = langCode;
	}
	public Integer getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(Integer orderNum) {
		this.orderNum = orderNum;
	}
	
}
