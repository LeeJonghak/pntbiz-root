package core.admin.authpackage.dao.domain;

public class AuthPackage {	
	private String packageName;
	private String packageOldName;
	
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}	
	public String getPackageOldName() {
		return packageOldName;
	}
	public void setPackageOldName(String packageOldName) {
		this.packageOldName = packageOldName;
	}	
}
